export const createPoint = (points) => {
    let pointList = []
    for (let i = 0; i < points.length; i++) {
        const geoPerson = new THREE.BoxGeometry(0.06, 0.06, 0.06);
        const matPeron = new THREE.MeshBasicMaterial({color: "red"});
        let point = new THREE.Mesh(geoPerson, matPeron);
        point.position.copy(points[i])
        point.position.y = 0

        pointList.push(point);
    }
    return pointList
};
